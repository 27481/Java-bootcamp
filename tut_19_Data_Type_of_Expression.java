public class tut_19_Data_Type_of_Expression {
    public static void main(String[] args) {
        int x=654+6;
        int y=6;
        short z=8;
        int a=y+z;
        float b=6.54f+6;
        System.out.println(b);
        System.out.println(a);
        System.out.println(z);
        System.out.println(x);
        
    }
}
