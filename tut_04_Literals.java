public class tut_04_Literals {
    public static void main(String[] args) {
        //! Literal -> A constant value which can be assinged to the variable is called as a literal
        /*

        */
        byte age =34;     //! here 34 is an integer literal
        char ch='A';      //! here A is character literal 
        float f1=5.6f;
        double d1=4.66d;

        System.out.println(age);   
        System.out.println(ch);   
        System.out.println(f1);   
        System.out.println(d1);   
    }         
}


