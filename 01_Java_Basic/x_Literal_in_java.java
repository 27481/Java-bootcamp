public class x_Literal_in_java {
    public static void main(String[] args) {
        //! Literal -> A constant value which can be assinged to the variable is called as a literal
        byte age =34;
        char ch='A';
        float f1=5.6f;
        System.out.println(age);
        System.out.println(ch);
        System.out.println(f1);
    }
}
